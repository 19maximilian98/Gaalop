package de.gaalop.gapp;

/**
 * Represents an index of a selector
 * @author Christian Steinmetz
 */
public class SelectorIndex {

    private int index;

    public SelectorIndex(int index) {
        this.index = index;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

}
